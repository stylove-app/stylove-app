function subtypeLabels(
  labels: Record<string, string>,
): Record<string, string> {
  const all = Object.values(WARDROBE_SUBCATEGORIES).flat();
  const out: Record<string, string> = {};
  for (const key of all) {
    out[key] = labels[key] ?? key;
  }
  return out;
}

